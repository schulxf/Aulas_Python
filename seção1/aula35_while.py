"""
Repetições
while (enquanto)
Executa uma ação enquanto uma condição for verdadeira
Loop infinito -> Quando um código não tem fim
"""

contador = 0

while contador < 10:  ## enquanto o contador for menor do que 10, continua executando
    contador = contador + 1  ## pega o valor atual da variavel e soma 1, até chegar no limite de 10
    print(contador)

print('Acabou')  ## só vai printar o acabou quando a repetição do while parar