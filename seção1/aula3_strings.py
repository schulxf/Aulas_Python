print(1234)

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"')

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'")

# Escape - a barra faz o python pular o prox. caractere
print("Luiz \"Otávio\"")

# r - mostra o escape
print(r"Luiz \"Otávio\"")